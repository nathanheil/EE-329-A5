/* --------------------------------------------------------------------------
 * EE 329 A5: SPI DAC Interface with Keypad Control
 *
 * Description:
 * This program receives 3-digit voltage values from a 4x4 keypad,
 * converts the value into millivolts, translates it into a 16-bit
 * SPI DAC command, and transmits it to the MCP4921 DAC.
 *
 * Authors: Nathan Heil, Cameron Becker
 * Date: May 10, 2025
 * MCU: STM32L4A6ZG (Nucleo Board)
 * Peripherals: Keypad on GPIOD, DAC via SPI1 (PA4â€“PA7)
 * -------------------------------------------------------------------------- */

#include "main.h"
#include "keypad.h"
#include "dac.h"

void SystemClock_Config(void);
static void MX_GPIO_Init(void);

int main(void) {

  HAL_Init();                        // Reset all peripherals, init Flash, configure SysTick
  SystemClock_Config();             // Configure system clock
  MX_GPIO_Init();                   // HAL-based GPIO init for LEDs, UART, etc.

  Keypad_Init();                    // Configure GPIOD for 4x4 keypad
  DAC_Init();                       // Configure GPIOA and SPI1 for DAC interface

  char digits[3];                   // Stores 3-digit voltage entry
  uint8_t index = 0;                // Tracks digit entry progress

  while (1) {
    int key = Keypad_GetKey();     // Poll for keypad input

    if (key >= 0 && key <= 9 && index < 3) {
      digits[index++] = key;       // Save digit if valid

      if (index == 3) {
        // Convert 3-digit input to millivolts (e.g. 275 = 2750 mV)
        uint16_t mV = 1000 * digits[0] + 100 * digits[1] + 10 * digits[2];

        uint16_t cmd = DAC_Convert(mV); // Build 16-bit DAC command word
        DAC_Write(cmd);                 // Send via SPI to MCP4921

        index = 0;                      // Reset for next voltage entry
      }

    } else if (key == 14) {            // '*' resets current entry
      index = 0;
    }
  }
}

